#include <stdio.h>
#include <cmath>


int main()
{
	
	float A= 4.0; 
	
	float B= 5.0; 
	
	float C= sqrt (A*A + B*B);
	
	printf("Di ketahui sebuah segitiga siku-siku dengan panjang sisi alas 4 cm dan sisi tinggi 5 cm.\n");
	
	printf("Dengan menggunakan rumus phitagoras untuk mencari sisi miring segitiga tersebut adalah.\n");
	
	printf("%.1f cm",C);
	
	return 0;
	 
}
